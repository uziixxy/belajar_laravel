 <h1 class="justify-text-center">Teka-teki Peran Ketum PP Japto Soerjosoemarno dalam Kasus Korupsi Rita Widyasari, Apa Kaitannya?</h1>
 <img src="img/pp.jpg" alt="Gedung" width="700">

 <h5>Ketua Umum Pemuda Pancasila (PP) Japto Soerjosoemarno menjadi sosok paling anyar terseret dalam kasus korupsi mantan Bupati Kutai Kartanegara, Kalimantan Timur, Rita Widyasari (RW). Rumah pribadi Japto yang berlokasi di Jalan Benda Ujung, RT 10 RW 01, Ciganjur, Jagakarsa, Jakarta Selatan, telah digeledah KPK pada Selasa (4/2/2025) malam. "Benar ada kegiatan penggeledahan perkara tersangka RW (Kukar) di rumah saudara JS," kata Juru Bicara KPK Tessa Mahardhika Sugiarto dalam keterangannya, Rabu (5/2/2025).
